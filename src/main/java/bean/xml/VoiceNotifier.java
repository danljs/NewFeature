package bean.xml;

public class VoiceNotifier {
	private String primaryContacts;
	private String informContacts;
	public String getPrimaryContacts() {
		return primaryContacts;
	}
	public void setPrimaryContacts(String primaryContacts) {
		this.primaryContacts = primaryContacts;
	}
	public String getInformContacts() {
		return informContacts;
	}
	public void setInformContacts(String informContacts) {
		this.informContacts = informContacts;
	}
}
